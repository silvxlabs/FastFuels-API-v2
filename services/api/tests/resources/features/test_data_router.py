STATIC_ROADS = "static-test-blue-mtn-osm-roads"
STATIC_WATER = "static-test-blue-mtn-osm-water"


# Helpers


def _load_static_template(static_name: str) -> dict:
    path = SHARED_TEST_FEATURES_DIR / f"{static_name}.json"
    if not path.exists():
        pytest.skip(
            f"{path} not found. Run the e2e fixture generator for {static_name}."
        )
    with open(path) as f:
        return json.load(f)


def _load_static_geojson(static_name: str) -> dict:
    """Read the static GeoJSON blob from bucket root for cross-checking."""
    fs = gcsfs.GCSFileSystem()
    url = f"{FEATURES_BUCKET}/{static_name}.geojson"
    if not fs.exists(url):
        pytest.skip(
            f"gs://{url} not found. Run the e2e fixture generator for {static_name}."
        )
    with fs.open(url, "rb") as f:
        return json.loads(f.read())


def data_route(domain_id: str, feature_id: str, **params):
    return f"/domains/{domain_id}/features/{feature_id}/data", params


def _install_static_feature(firestore_client, test_owner_id, domain_id, static_name):
    """Copy the bucket-root static blob into the test domain + register Firestore doc."""
    fs = gcsfs.GCSFileSystem()
    src = f"{FEATURES_BUCKET}/{static_name}.geojson"
    dst = f"{FEATURES_BUCKET}/{domain_id}/{static_name}.geojson"
    if not fs.exists(src):
        pytest.skip(
            f"gs://{src} not found. Run the e2e fixture generator for {static_name}."
        )
    if fs.exists(dst):
        fs.rm(dst)
    fs.cp(src, dst)

    template = _load_static_template(static_name)
    template["id"] = static_name
    template["owner_id"] = test_owner_id
    template["domain_id"] = domain_id
    doc_ref = firestore_client.collection(FEATURES_COLLECTION).document(static_name)
    doc_ref.set(template)
    return template, doc_ref, dst


def _uninstall_static_feature(doc_ref, blob_path):
    fs = gcsfs.GCSFileSystem()
    doc_ref.delete()
    if fs.exists(blob_path):
        fs.rm(blob_path)


