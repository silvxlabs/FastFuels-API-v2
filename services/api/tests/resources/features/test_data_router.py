    @pytest.mark.asyncio
    async def test_malformed_geojson_returns_422(self, monkeypatch):
        """Call the handler directly with a monkeypatched cache that raises
        InvalidFeatureGeoJSON. In-process — the live server isn't involved
        because monkeypatch can't cross process boundaries."""

        async def _ok_doc(*args, **kwargs):
            return None, SimpleNamespace(
                to_dict=lambda: {
                    "id": "feat-x",
                    "domain_id": "dom-1",
                    "status": "completed",
                }
            )

        async def _broken(domain_id, feature_id, page, size):
            raise InvalidFeatureGeoJSON("simulated malformed feature collection")

        monkeypatch.setattr(features_router, "get_document_async", _ok_doc)
        monkeypatch.setattr(features_router, "fetch_feature_page", _broken)

        with pytest.raises(HTTPException) as exc:
            await features_router.get_feature_data(
                request=SimpleNamespace(state=SimpleNamespace(id="owner-1")),
                domain={"id": "dom-1"},
                feature_id="feat-x",
                page=0,
                size=1000,
            )
        assert exc.value.status_code == 422
        assert "malformed" in exc.value.detail.lower()

    @pytest.mark.asyncio
    async def test_413_when_page_exceeds_size_cap(self, monkeypatch):
        """Call the handler directly with a monkeypatched cache that raises
        PageTooLarge — exercises the router's 413 mapping in-process."""

        async def _ok_doc(*args, **kwargs):
            return None, SimpleNamespace(
                to_dict=lambda: {
                    "id": "feat-x",
                    "domain_id": "dom-1",
                    "status": "completed",
                }
            )

        async def _too_large(domain_id, feature_id, page, size):
            raise PageTooLarge(50_000_000, features_cache.MAX_GEOJSON_BYTES)

        monkeypatch.setattr(features_router, "get_document_async", _ok_doc)
        monkeypatch.setattr(features_router, "fetch_feature_page", _too_large)

        with pytest.raises(HTTPException) as exc:
            await features_router.get_feature_data(
                request=SimpleNamespace(state=SimpleNamespace(id="owner-1")),
                domain={"id": "dom-1"},
                feature_id="feat-x",
                page=0,
                size=1000,
            )
        assert exc.value.status_code == 413
        assert "smaller" in exc.value.detail.lower()

    def test_pagination_headers_present_on_every_page(
        self, client, domain_for_testing, static_roads_in_firestore
    ):
        """Every paged response must carry the full X-* header set."""
        url, params = data_route(domain_for_testing["id"], STATIC_ROADS, size=2)
        response = client.get(url, params=params)
        assert response.status_code == 200
        for header in (
            "X-Page",
            "X-Page-Size",
            "X-Num-Features",
            "X-Total-Features",
            "X-Has-More",
        ):
            assert header in response.headers
        # Types: ints stringified; X-Has-More is "true" or "false"
        assert response.headers["X-Page"].isdigit()
        assert response.headers["X-Page-Size"].isdigit()
        assert response.headers["X-Num-Features"].isdigit()
        assert response.headers["X-Total-Features"].isdigit()
        assert response.headers["X-Has-More"] in ("true", "false")
