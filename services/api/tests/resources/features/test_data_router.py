    @pytest.mark.asyncio
    async def test_malformed_parquet_returns_422(self, monkeypatch):
        """Direct-call test: a monkeypatched cache that raises
        InvalidFeatureParquet maps to a 422 with an explanatory detail.
        In-process — monkeypatch can't cross the live-server boundary."""

        async def _ok_doc(*args, **kwargs):
            return None, SimpleNamespace(
                to_dict=lambda: {
                    "id": "feat-x",
                    "domain_id": "dom-1",
                    "status": "completed",
                }
            )

        async def _broken(domain_id, feature_id, partition_index):
            raise InvalidFeatureParquet("simulated malformed parquet")

        monkeypatch.setattr(features_router, "get_document_async", _ok_doc)
        monkeypatch.setattr(features_router, "fetch_partition_geojson", _broken)

        with pytest.raises(HTTPException) as exc:
            await features_router.get_feature_data_partition(
                request=SimpleNamespace(state=SimpleNamespace(id="owner-1")),
                domain={"id": "dom-1"},
                feature_id="feat-x",
                partition_index=0,
            )
        assert exc.value.status_code == 422
        assert "malformed" in exc.value.detail.lower()

    @pytest.mark.asyncio
    async def test_413_when_partition_exceeds_size_cap(self, monkeypatch):
        """Direct-call test: PartitionTooLarge maps to 413."""

        async def _ok_doc(*args, **kwargs):
            return None, SimpleNamespace(
                to_dict=lambda: {
                    "id": "feat-x",
                    "domain_id": "dom-1",
                    "status": "completed",
                }
            )

        async def _too_large(domain_id, feature_id, partition_index):
            raise PartitionTooLarge(50_000_000, features_cache.MAX_RESPONSE_BYTES)

        monkeypatch.setattr(features_router, "get_document_async", _ok_doc)
        monkeypatch.setattr(features_router, "fetch_partition_geojson", _too_large)

        with pytest.raises(HTTPException) as exc:
            await features_router.get_feature_data_partition(
                request=SimpleNamespace(state=SimpleNamespace(id="owner-1")),
                domain={"id": "dom-1"},
                feature_id="feat-x",
                partition_index=0,
            )
        assert exc.value.status_code == 413
        assert "re-create" in exc.value.detail.lower()
