"""
Integration tests for the feature data streaming endpoint.

Endpoint under test:
  GET /domains/{domain_id}/features/{feature_id}/data?page=&size=

These tests run against static GeoJSON blobs at
``gs://{FEATURES_BUCKET}/static-test-blue-mtn-osm-{roads,water}.geojson``
generated by ``tests/e2e/test_create_fixtures.py``. The session-scoped
Firestore fixtures copy each static blob into the test domain so the API's
domain-prefixed blob resolver finds it.

Cache-dependent error cases (missing blob, malformed GeoJSON, oversize page)
use ``monkeypatch`` to swap the cache function on the router module — the
same pattern grids/inventories use to avoid flushing the ``ring.lru``
storage between tests.
"""

