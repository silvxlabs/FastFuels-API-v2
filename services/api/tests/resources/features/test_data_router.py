# Tests


class TestGetFeatureData:
    """Test GET /domains/{domain_id}/features/{feature_id}/data."""

    def test_returns_200_default_page(
        self,
        client,
        domain_for_testing,
        static_roads_in_firestore,
        static_roads_geojson,
    ):
        url, params = data_route(domain_for_testing["id"], STATIC_ROADS)
        response = client.get(url, params=params)
        assert response.status_code == 200, response.text
        assert response.headers["content-type"].startswith("application/geo+json")

        body = response.json()
        assert body["type"] == "FeatureCollection"
        expected_total = len(static_roads_geojson["features"])
        assert len(body["features"]) == expected_total

        assert response.headers["X-Page"] == "0"
        assert response.headers["X-Page-Size"] == "1000"
        assert response.headers["X-Num-Features"] == str(expected_total)
        assert response.headers["X-Total-Features"] == str(expected_total)
        assert response.headers["X-Has-More"] == "false"

    def test_response_body_matches_local_geojson(
        self,
        client,
        domain_for_testing,
        static_roads_in_firestore,
        static_roads_geojson,
    ):
        """Round-trip: concatenated pages reproduce the source feature list in order."""
        size = 2
        page = 0
        collected = []
        seen_total = None
        while True:
            url, params = data_route(
                domain_for_testing["id"], STATIC_ROADS, page=page, size=size
            )
            response = client.get(url, params=params)
            assert response.status_code == 200, response.text
            body = response.json()
            collected.extend(body["features"])
            seen_total = int(response.headers["X-Total-Features"])
            if response.headers["X-Has-More"] == "false":
                break
            page += 1

        assert seen_total == len(static_roads_geojson["features"])
        assert len(collected) == seen_total
        # Order must be preserved (the byte-range splice keeps source order).
        assert [json.dumps(f, sort_keys=True) for f in collected] == [
            json.dumps(f, sort_keys=True) for f in static_roads_geojson["features"]
        ]

    def test_preserves_top_level_crs(
        self,
        client,
        domain_for_testing,
        static_roads_in_firestore,
        static_roads_geojson,
    ):
        """The top-level `crs` block (if present in the source) is preserved
        verbatim in the response body — byte-range splice gives this for free."""
        url, params = data_route(domain_for_testing["id"], STATIC_ROADS, size=2)
        response = client.get(url, params=params)
        assert response.status_code == 200
        body = response.json()
        # OSM roads GeoJSON includes a `crs` block; both source and response
        # should agree (or both should lack one).
        assert body.get("crs") == static_roads_geojson.get("crs")

    def test_water_endpoint_returns_200(
        self, client, domain_for_testing, static_water_in_firestore
    ):
        url, params = data_route(domain_for_testing["id"], STATIC_WATER)
