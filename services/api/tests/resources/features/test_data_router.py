"""
Integration tests for the feature data streaming endpoints.

Endpoints under test:
  GET /domains/{domain_id}/features/{feature_id}/data/metadata
  GET /domains/{domain_id}/features/{feature_id}/data/{partition_index}

These tests run against static GeoParquet blobs at
``gs://{FEATURES_BUCKET}/static-test-blue-mtn-osm-{roads,water}.parquet``
generated by ``tests/e2e/test_create_fixtures.py``. The session-scoped
Firestore fixtures copy each static blob into the test domain so the
API's domain-prefixed blob resolver finds it.

Cache-dependent error cases (missing blob, malformed Parquet, oversize
partition) use ``monkeypatch`` to swap the cache function on the router
module — the same pattern grids/inventories use to avoid flushing the
``ring.lru`` storage between tests.
"""

import json
from types import SimpleNamespace
from uuid import uuid4

